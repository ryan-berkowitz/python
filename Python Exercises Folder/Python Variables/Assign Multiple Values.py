#Assign multiple variables in one line:
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

#Assign multile variables to one value
a = b = c = "Blueberry"
print(a)
print(b)
print(c)

#Unpack a collection
fruits = ["apple", "banana", "cherry"]
d, e, f = fruits
print(d)
print(e)
print(f)